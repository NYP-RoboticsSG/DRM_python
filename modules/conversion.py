
# pound = 2.204 * kg
def kg_to_pound(weight_in_kg):
    weight_in_pound = 2.204 * weight_in_kg
    return weight_in_pound

# fahrenheit = 1.8 * celsius + 32
def celsius_to_fahrenheit(celsius):
    fahrenheit = celsius * 1.8 + 32
    return fahrenheit

# feet = 3.28 * meter
def meter_to_feet(meter):
    height = meter * 3.28
    return height
